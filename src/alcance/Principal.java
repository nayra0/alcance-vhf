    private static void calcAlcance(Alcance a) {
        double aux = Math.sqrt(2 * RAIO_TERRA);
        double dist1 = aux * Math.sqrt(a.getAlt1());
        double dist2 = aux * Math.sqrt(a.getAlt2());
        a.setDist1(dist1);
        a.setDist2(dist2);
        a.setDist(dist1 + dist2);
    }

    private static void calcAltura(Alcance a) {
        double aux = Math.sqrt(2 * RAIO_TERRA);
        double alt1 = Math.pow((a.getDist1() / aux), 2);
        double alt2 = Math.pow((a.getDist2() / aux), 2);
        a.setAlt1(alt1);
        a.setAlt2(alt2);
        a.setDist(a.getDist1() + a.getDist2());
    }

    private class Alcance {

        double alt1;
        double alt2;
        double dist1;
        double dist2;
        double dist;

        public double getAlt1() {
            return alt1;
        }

        public void setAlt1(double alt1) {
            this.alt1 = alt1;
        }

        public double getAlt2() {
            return alt2;
        }

        public void setAlt2(double alt2) {
            this.alt2 = alt2;
        }

        public double getDist1() {
            return dist1;
        }

        public void setDist1(double dist1) {
            this.dist1 = dist1;
        }

        public double getDist2() {
            return dist2;
        }

        public void setDist2(double dist2) {
            this.dist2 = dist2;
        }

        public double getDist() {
            return dist;
        }

        public void setDist(double dist) {
            this.dist = dist;
        }

    }

