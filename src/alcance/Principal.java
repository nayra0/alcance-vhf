    private void jButtonCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcActionPerformed
        // TODO add your handling code here:
        
        

        String ant1 = this.jTextFieldAnt1.getText();
        String ant2 = this.jTextFieldAnt2.getText();
        String alc1 = this.jTextFieldAlc1.getText();
        String alc2 = this.jTextFieldAlc2.getText();
        String alc = this.jTextFieldAlc.getText();

        Alcance a = new Alcance();

        if (!ant1.isEmpty() && !ant2.isEmpty()) {
            a.setAlt1(Double.parseDouble(ant1));
            a.setAlt2(Double.parseDouble(ant2));
            calcAlcance(a);
        } else if (!ant1.isEmpty() && ant2.isEmpty()) {
            a.setAlt1(Double.parseDouble(ant1));
            a.setAlt2(0);
            calcAlcance(a);
        } else if (ant1.isEmpty() && !ant2.isEmpty()) {
            a.setAlt2(Double.parseDouble(ant2));
            a.setAlt1(0);
            calcAlcance(a);
        } else if (ant1.isEmpty() && ant2.isEmpty()) {
            a.setAlt1(0);
            a.setAlt2(0);
            if (!alc1.isEmpty() && !alc2.isEmpty()) {
                a.setDist1(Double.parseDouble(alc1));
                a.setDist2(Double.parseDouble(alc2));
                calcAltura(a);
            } else if (!alc1.isEmpty() && alc2.isEmpty()) {
                a.setDist1(Double.parseDouble(alc1));
                a.setDist2(0);
                calcAltura(a);
            } else if (alc1.isEmpty() && !alc2.isEmpty()) {
                a.setDist2(Double.parseDouble(alc2));
                a.setDist1(0);
                calcAltura(a);
            } else if (!alc.isEmpty()) {
                a.setDist1(Double.parseDouble(alc) / 2);
                a.setDist2(a.getDist1());
                calcAltura(a);
            }
        }

        this.jTextFieldAnt1.setText(String.format(new Locale("pt-br"), "%.4f", a.getAlt1()));
        this.jTextFieldAnt2.setText(String.format(new Locale("pt-br"), "%.4f", a.getAlt2()));
        this.jTextFieldAlc.setText(String.format(new Locale("pt-br"), "%.4f", a.getDist()));
        this.jTextFieldAlc.setText(String.format(new Locale("pt-br"), "%.4f", a.getDist()));
        this.jTextFieldAlc1.setText(String.format(new Locale("pt-br"), "%.4f", a.getDist1()));
        this.jTextFieldAlc2.setText(String.format(new Locale("pt-br"), "%.4f", a.getDist2()));


    }//GEN-LAST:event_jButtonCalcActionPerformed

